package javap.dz_chain;

public class Triangle {
    private final int ab;
    private final int bc;
    private final int ac;

    public Triangle(int ab, int bc, int ac) {
        this.ab = ab;
        this.bc = bc;
        this.ac = ac;
    }

    public int getAb() {
        return ab;
    }

    public int getBc() {
        return bc;
    }

    public int getAc() {
        return ac;
    }
}
